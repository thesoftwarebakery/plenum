/// Generates `sdk/plenum-generated.ts` from the Rust type definitions.
///
/// Run from the repo root:
///   cargo run -p plenum-core --bin export-types
///
/// This is invoked automatically by `e2e/package.json`'s `pretest` script.
use plenum_core::{
    config::PermissionsConfig,
    interceptor::{
        ErrorContext, GatewayErrorInput, InterceptorOutput, JsInterceptorOutput, JsRequestInput,
        JsResponseInput,
    },
    rate_limit::RateLimitState,
    upstream_plugin::{JsPluginInput, PluginOutput, PluginRequest},
};
use std::fmt::Write as _;
use ts_rs::TS;

fn main() {
    let out_path = concat!(env!("CARGO_MANIFEST_DIR"), "/../sdk/plenum-generated.ts");

    let mut buf = String::new();
    writeln!(
        buf,
        "// @generated by `cargo run -p plenum-core --bin export-types`"
    )
    .unwrap();
    writeln!(
        buf,
        "// Do not edit manually — run the command above to regenerate."
    )
    .unwrap();
    writeln!(buf).unwrap();
    writeln!(buf, "import type {{ Ctx }} from \"./plenum\";").unwrap();
    writeln!(buf).unwrap();

    for decl in [
        PermissionsConfig::decl(),
        RateLimitState::decl(),
        JsRequestInput::decl(),
        JsResponseInput::decl(),
        InterceptorOutput::decl(),
        JsInterceptorOutput::decl(),
        PluginRequest::decl(),
        JsPluginInput::decl(),
        PluginOutput::decl(),
        ErrorContext::decl(),
        GatewayErrorInput::decl(),
    ] {
        writeln!(buf, "export {decl}").unwrap();
        writeln!(buf).unwrap();
    }

    std::fs::write(out_path, buf).expect("failed to write sdk/plenum-generated.ts");
    println!("wrote {out_path}");
}
